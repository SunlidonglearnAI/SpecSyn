#!/usr/bin/env python3
from __future__ import annotations

import argparse
import csv
import os
import re
from collections import defaultdict
from pathlib import Path


def parse_args():
    parser = argparse.ArgumentParser(
        description=(
            "Create a non-destructive, category-based symlink index for "
            "myosuite/agents/outputs training results."
        )
    )
    parser.add_argument("--root", type=Path, default=Path(__file__).resolve().parents[1])
    parser.add_argument(
        "--audit-csv",
        type=Path,
        default=None,
        help="CSV generated by scripts/audit_rl_model_candidates.py.",
    )
    parser.add_argument(
        "--index-dir",
        type=Path,
        default=None,
        help="Output symlink index directory. Defaults to myosuite/agents/outputs_by_category.",
    )
    parser.add_argument(
        "--include-unmatched",
        action="store_true",
        help="Also index model runs that do not belong to the 63-task matrix.",
    )
    return parser.parse_args()


def safe_name(text: str) -> str:
    text = text.strip().replace(os.sep, "__")
    text = re.sub(r"[^A-Za-z0-9_.=-]+", "_", text)
    return text.strip("_") or "unknown"


def relative_to_outputs(path: Path, outputs_root: Path) -> str:
    try:
        return str(path.relative_to(outputs_root))
    except ValueError:
        return path.name


def make_symlink(link_path: Path, target: Path) -> str:
    link_path.parent.mkdir(parents=True, exist_ok=True)
    rel_target = os.path.relpath(target, link_path.parent)
    if link_path.is_symlink():
        current = os.readlink(link_path)
        if current == rel_target:
            return "kept"
        return "exists_different"
    if link_path.exists():
        return "exists_not_symlink"
    link_path.symlink_to(rel_target, target_is_directory=target.is_dir())
    return "created"


def read_rows(path: Path):
    with path.open("r", newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def best_by_env(rows):
    grouped = defaultdict(list)
    for row in rows:
        if row.get("group"):
            grouped[row["canonical_env"]].append(row)
    out = {}
    for env, candidates in grouped.items():
        candidates.sort(
            key=lambda row: (
                int(row.get("score") or 0),
                row.get("referenced_by_report", "") != "",
                float(row.get("model_size_mb") or 0.0),
            ),
            reverse=True,
        )
        out[env] = candidates[0]
    return out


def main():
    args = parse_args()
    root = args.root.resolve()
    audit_csv = args.audit_csv or root / "paper" / "rl_model_candidate_audit.csv"
    index_dir = args.index_dir or root / "myosuite" / "agents" / "outputs_by_category"
    outputs_root = root / "myosuite" / "agents" / "outputs"

    if not audit_csv.exists():
        raise FileNotFoundError(
            f"Audit CSV not found: {audit_csv}. Run scripts/audit_rl_model_candidates.py first."
        )

    rows = read_rows(audit_csv)
    recommended = best_by_env(rows)
    manifest_rows = []
    created = kept = skipped = 0

    for row in rows:
        group = row.get("group") or ("unmatched" if args.include_unmatched else "")
        if not group:
            skipped += 1
            continue

        model_path = Path(row["model_path"])
        run_dir = model_path.parent
        if not run_dir.exists():
            skipped += 1
            continue

        env = row["canonical_env"] or row["env_id"]
        seed = row.get("seed") or "unknown"
        score = row.get("score") or "0"
        run_rel = relative_to_outputs(run_dir, outputs_root)
        link_name = safe_name(f"score={score}__seed={seed}__{run_rel}")
        link_path = index_dir / group / env / "candidates" / link_name
        status = make_symlink(link_path, run_dir)
        if status == "created":
            created += 1
        elif status == "kept":
            kept += 1
        else:
            skipped += 1

        is_recommended = recommended.get(env, {}).get("model_path") == row["model_path"]
        if is_recommended:
            rec_link = index_dir / group / env / "recommended"
            rec_status = make_symlink(rec_link, run_dir)
            if rec_status == "created":
                created += 1
            elif rec_status == "kept":
                kept += 1

        manifest_rows.append(
            {
                "group": group,
                "env": env,
                "is_recommended": "yes" if is_recommended else "no",
                "score": score,
                "seed": seed,
                "link_path": str(link_path),
                "original_run_dir": str(run_dir),
                "model_path": row["model_path"],
                "vecnormalize_path": row.get("vecnormalize_path", ""),
                "referenced_by_report": row.get("referenced_by_report", ""),
            }
        )

    index_dir.mkdir(parents=True, exist_ok=True)
    manifest = index_dir / "MANIFEST.csv"
    with manifest.open("w", newline="", encoding="utf-8") as handle:
        fieldnames = [
            "group",
            "env",
            "is_recommended",
            "score",
            "seed",
            "link_path",
            "original_run_dir",
            "model_path",
            "vecnormalize_path",
            "referenced_by_report",
        ]
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(manifest_rows)

    readme = index_dir / "README.md"
    readme.write_text(
        "\n".join(
            [
                "# Outputs By Category",
                "",
                "This directory is a non-destructive symlink index for `myosuite/agents/outputs`.",
                "The original training folders are not moved, renamed, copied, or edited.",
                "",
                "Layout:",
                "",
                "- `<group>/<env>/recommended` points to the highest-scored candidate run.",
                "- `<group>/<env>/candidates/*` points to all candidate run directories for that env.",
                "- `MANIFEST.csv` records each link and its original path.",
                "",
                "Regenerate with:",
                "",
                "```bash",
                "python scripts/audit_rl_model_candidates.py",
                "python scripts/classify_outputs_by_experiment.py",
                "```",
            ]
        )
        + "\n",
        encoding="utf-8",
    )

    print(f"Index dir: {index_dir}")
    print(f"Manifest: {manifest}")
    print(f"Symlinks created={created}, kept={kept}, skipped={skipped}")


if __name__ == "__main__":
    main()
